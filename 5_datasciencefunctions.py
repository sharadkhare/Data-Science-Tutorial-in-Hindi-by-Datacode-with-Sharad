# data science function: the most used functions in DS is max(), min() and mean()
# the max() function: to find the highest value in an array.
Average_Pulse = max(80,85,90,95,100,105,110,115,120,125)
print(Average_Pulse)
# the min() function: to find the lowest value in an array
Average_Pulse = min(80,85,90,95,100,105,110,115,120,125)
print(Average_Pulse)

# the mean () function
import numpy as np
Calorie_burnage = [240,250,260,270,280,290,300,310,320,330]
average_Calorie_burnage = np.mean(Calorie_burnage)
print(average_Calorie_burnage)
 